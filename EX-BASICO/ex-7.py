# 8. Faça um algoritmo que receba 4 números e
# realize a soma deles e a média entre eles. e
# mostre os resultados.

num_1 = int(input("NUMERO 1: "))
num_2 = int(input("NUMERO 2: "))
num_3 = int(input("NUMERO 3: "))
num_4 = int(input("NUMERO 4: "))
media = (num_1 + num_2 + num_3 + num_4) / 4
print(f"A MEDIAGERAL É: {media}")